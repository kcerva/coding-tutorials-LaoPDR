def cost_calculation_external(qty,price):
  """This function multiplies the quantity of items
  by the unit prices and prints total cost.

  Inputs
  qty - item quantity
  price - unit price

  Outputs
  Total cost print to screen
  """
  # Calculation
  cost = qty * price
  # Output
  print(cost) 